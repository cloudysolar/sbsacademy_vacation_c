#include <stdio.h>

int lesson2_3() {
	// 비교 연산자
	int a = 10;
	int b = 5;

	printf("a = %d / b = %d\n\n", a, b);

	printf("a > b = %d\n", (a > b));
	printf("a >= b = %d\n", (a >= b));
	printf("a < b = %d\n", (a < b));
	printf("a <= b = %d\n", (a <= b));
	printf("a == b = %d\n", (a == b));
	printf("a != b = %d\n", (a != b));

	return 0;
}
